#	We create 3 variables a : int, b: float, c: string.  
a=3 # or a = int(3)
b=4.7 # or a = float(4.7)
c="Hello" # or c= str("Hello")
#	We display these variables using the print() function
print(a,b,c)
#   We display the types of these 3 variables using the type() function  
print(type(a), type(b),type(c))

# We transform a from int to float
a=float(a)
# We transform b from float to int
b=int(b)
# We display the variables and their new types
print (a,type(a))
print (b,type(b))